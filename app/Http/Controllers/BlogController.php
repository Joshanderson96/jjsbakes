    public function store(Request $request)
    {
        $validateData = $request->validate([
            'title' => 'required',
            'user_id' => 'reqired',
            'hours' => 'required',
            'minutes' => 'required',
            'recipe' => 'required',
            'post' => 'required',
        ]);

        $validateData['user_id'] = Auth::user()->id;

        Blog::create($validateData);
        

        return redirect('blogs');
    }

